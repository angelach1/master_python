# Operadores de asignación
edad = 25  # Asignación simple
print("Edad inicial:", edad)
edad += 5  # Incremento
print("Edad después de incrementar 5:", edad)
edad -= 3  # Decremento
print("Edad después de decrementar 3:", edad)
edad *= 2  # Multiplicación
print("Edad después de multiplicar por 2:", edad)
edad /= 4  # División
print("Edad después de dividir por 4:", edad)
edad %= 6  # Módulo
print("Edad después de módulo 6:", edad)
edad **= 2  # Exponenciación
print("Edad después de elevar al cuadrado:", edad)
edad //= 3  # División entera
print("Edad después de división entera por 3:", edad)

# Operadores de incremento y decremento
year = 2024
print("Año inicial:", year)
year += 1  # Incremento
print("Año después de incrementar 1:", year)
year -= 1  # Decremento
print("Año después de decrementar 1:", year)
year += 5  # Incremento en 5
print("Año después de incrementar 5:", year)
