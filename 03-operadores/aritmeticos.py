# Operadores aritméticos
numero1 = 77
numero2 = 44 # operador asignación =

resta = numero1 - numero2 # operador resta -
multiplicacion = numero1 * numero2 # operador multiplicación *
division = numero1 / numero2 # operador división /

print("******************** CALCULADORA ********************")

print (f"laresta de {numero1} - {numero2} es: {resta}") # operador resta -
print (f"la suma de {numero1} + {numero2} es: {numero1 + numero2}") # operador suma +
print (f"la multiplicación de {numero1} * {numero2} es: {multiplicacion}") # operador multiplicación *
print (f"la división de {numero1} / {numero2} es: {division}") # operador división /
print (f"la división entera de {numero1} // {numero2} es: {numero1 // numero2}") # operador división entera //
print (f"el módulo de {numero1} % {numero2} es: {numero1 % numero2}") # operador módulo %
print (f"el exponente de {numero1} ** {numero2} es: {numero1 ** numero2}") # operador exponente **
print("****************************************************")



