'''
Ejercicio 8:
¿Cuanto es el X por ciento de un número Y?
No se permite usar funciones

'''

x = float(input("Ingrese el valor de X (porcentaje): "))
y = float(input("Ingrese el valor de Y (número): "))
resultado = (x / 100) * y # Calcula el X% de Y
print(f"El {x}% de {y} es: {resultado}")
# Fin del programa
